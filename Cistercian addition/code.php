function getCistercian(int $value): array {
    $cistercian = array_fill(0, 5, array_fill(0, 5, ' '));
    $cistercian[1][2] = $cistercian[2][2] = $cistercian[3][2] = '|';

    $thousands = intdiv($value, 1000);
    $hundreds  = intdiv($value % 1000, 100);
    $tens      = intdiv($value % 100, 10);
    $units     = $value % 10;

    foreach ([$units, $tens * 10, $hundreds * 100, $thousands * 1000] as $part) {
        foreach (CISTERCIAN[$part] ?? [] as [$y, $x, $c]) $cistercian[$y][$x] = $c;
    }

    return $cistercian;
}

function getValue(array $cistercian): int {
    $value = 0;

    foreach(CISTERCIAN as $v => $list) {
        foreach($list as [$y, $x, $c]) {
            if($cistercian[$y][$x] != $c) continue 2;
        }

        $value += $v;
    }

    return $value;
}

function readNumber(): array {
    $number = [];

    for ($i = 0; $i < 5; $i++) {
        $number[] = preg_split('//u', stream_get_line(STDIN, 10, "\n"), -1, PREG_SPLIT_NO_EMPTY);
    }

    return $number;
}

